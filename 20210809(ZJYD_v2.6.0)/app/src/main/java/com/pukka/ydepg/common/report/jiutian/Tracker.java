public class Tracker {

    private String playTracer;

    private String displayTracer;

    private String clickTracer;

    public String getPlayTracer() {
        return playTracer;
    }

    public void setPlayTracer(String playTracer) {
        this.playTracer = playTracer;
    }

    public String getDisplayTracer() {
        return displayTracer;
    }

    public void setDisplayTracer(String displayTracer) {
        this.displayTracer = displayTracer;
    }

    public String getClickTracer() {
        return clickTracer;
    }

    public void setClickTracer(String clickTracer) {
        this.clickTracer = clickTracer;
    }
}
